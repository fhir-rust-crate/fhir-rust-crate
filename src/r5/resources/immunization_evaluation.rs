/// ImmunizationEvaluation describes a comparison of an immunization event
/// against published recommendations to determine if the administration is
/// "valid" in relation to those recommendations. It captures whether a given
/// dose counts toward a vaccine series and, if not, the reason why. This
/// resource is typically produced by clinical decision support systems that
/// assess a patient's immunization history against a schedule.
///
/// Clinically, an evaluation is generated by comparing a single recorded
/// immunization event against an authoritative vaccination schedule (such as
/// one published by a national or regional immunization advisory body) to
/// determine the dose's status, its position within a series, and whether it
/// counts toward completing that series. This supports use cases such as
/// forecasting future doses, reconciling immunization histories, and
/// generating official immunization records or certificates. Unlike a
/// recommendation for future care, an ImmunizationEvaluation is a
/// retrospective assessment of a dose that has already been administered.
///
/// Related resources: the evaluated dose is referenced from an
/// `Immunization` resource, the subject is typically a
/// [`Patient`](crate::r5::resources::patient::Patient), and coded values such
/// as the target disease and dose status use
/// [`CodeableConcept`](crate::r5::types::CodeableConcept). See also the
/// related `ImmunizationRecommendation` resource, which is forward-looking
/// rather than retrospective.
///
/// # Examples
///
/// ```
/// use fhir::r5::resources::immunization_evaluation::ImmunizationEvaluation;
///
/// let value = ImmunizationEvaluation::default();
/// let json = ::serde_json::to_value(&value).unwrap();
/// let back: ImmunizationEvaluation = ::serde_json::from_value(json).unwrap();
/// assert_eq!(value, back);
/// ```
